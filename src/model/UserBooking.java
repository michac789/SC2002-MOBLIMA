package model;

public class UserBooking {
    private int userId;
    private String title;
    private String location;
    private int cinemaCode;
    private String datetime;
    private String seat;

    public UserBooking (int userId, String title, String location, int cinemaCode, String datetime, String seat) {
        this.userId = userId;
        this.title = title;
        this.location = location;
        this.cinemaCode = cinemaCode;
        this.datetime = datetime;
        this.seat = seat;
    }








}
